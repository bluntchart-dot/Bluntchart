// ─── CANVAS CARD RENDERER — 1080×1920 PNG for Stories ─────────────────────────

function drawCardToCanvas(fname: string, card: ShareCard): HTMLCanvasElement {
  const W = 1080, H = 1920;
  const cvs = document.createElement("canvas");
  cvs.width = W; cvs.height = H;
  const ctx = cvs.getContext("2d")!;

  const wrapText = (text: string, maxW: number): string[] => {
    const words = text.split(" "); const lines: string[] = []; let line = "";
    for (const w of words) {
      const t = line ? `${line} ${w}` : w;
      if (ctx.measureText(t).width > maxW && line) { lines.push(line); line = w; } else line = t;
    }
    if (line) lines.push(line); return lines;
  };

  const PAD = 88, CW = W - PAD*2;

  const bg = ctx.createLinearGradient(0,0,0,H);
  bg.addColorStop(0,"#08070f"); bg.addColorStop(0.35,"#0c0a1e");
  bg.addColorStop(0.7,"#0e0628"); bg.addColorStop(1,"#08070f");
  ctx.fillStyle = bg; ctx.fillRect(0,0,W,H);

  const g1 = ctx.createRadialGradient(180,320,0,180,320,680);
  g1.addColorStop(0,"rgba(107,47,212,0.14)"); g1.addColorStop(1,"rgba(107,47,212,0)");
  ctx.fillStyle = g1; ctx.fillRect(0,0,W,H);

  for (const [sx,sy,sr] of [[90,160,2.2],[950,240,1.6],[72,510,1.4],[1002,800,1.8],
    [68,1100,1.4],[870,1240,1.6],[220,1380,2.0],[760,1560,1.4],[440,1750,1.6]]) {
    ctx.beginPath(); ctx.arc(sx,sy,sr,0,Math.PI*2);
    ctx.fillStyle="rgba(255,255,255,0.25)"; ctx.fill();
  }

  const topBar = ctx.createLinearGradient(0,0,W,0);
  topBar.addColorStop(0,"#6b2fd4"); topBar.addColorStop(0.5,"#d4537e"); topBar.addColorStop(1,"#f0b84a");
  ctx.fillStyle = topBar; ctx.fillRect(0,0,W,10);

  ctx.textAlign="left"; ctx.font="600 34px Georgia,serif";
  ctx.fillStyle="rgba(240,184,74,0.8)"; ctx.fillText("✦  BluntChart",PAD,170);
  ctx.font="400 26px sans-serif"; ctx.fillStyle="rgba(232,228,240,0.2)";
  ctx.fillText("your chart read you first.",PAD+310,170);

  const rg = ctx.createLinearGradient(PAD,0,W-PAD,0);
  rg.addColorStop(0,"rgba(107,47,212,0)"); rg.addColorStop(0.4,"rgba(107,47,212,0.4)"); rg.addColorStop(1,"rgba(107,47,212,0)");
  ctx.strokeStyle=rg; ctx.lineWidth=1;
  ctx.beginPath(); ctx.moveTo(PAD,200); ctx.lineTo(W-PAD,200); ctx.stroke();

  let nfs = 152; ctx.font=`700 ${nfs}px Georgia,serif`;
  while (ctx.measureText(fname.toUpperCase()).width>CW && nfs>72) { nfs-=4; ctx.font=`700 ${nfs}px Georgia,serif`; }
  ctx.fillStyle="#f0ece8"; ctx.textAlign="center"; ctx.fillText(fname.toUpperCase(),W/2,500);

  const nw=ctx.measureText(fname.toUpperCase()).width;
  const ug=ctx.createLinearGradient(W/2-Math.min(nw/2,180),0,W/2+Math.min(nw/2,180),0);
  ug.addColorStop(0,"rgba(240,184,74,0)"); ug.addColorStop(0.5,"rgba(240,184,74,0.65)"); ug.addColorStop(1,"rgba(240,184,74,0)");
  ctx.strokeStyle=ug; ctx.lineWidth=2.5;
  ctx.beginPath(); ctx.moveTo(W/2-Math.min(nw/2,180),526); ctx.lineTo(W/2+Math.min(nw/2,180),526); ctx.stroke();

  const ls = [
    {font:"700 62px Georgia,serif",col:"flex",la:30},
    {font:"500 54px sans-serif",col:"sting",la:36},
    {font:"italic 400 44px Georgia,serif",col:"warm",la:0},
  ];
  let ly=650;
  for (let i=0;i<Math.min(card.lines.length,3);i++) {
    const line=card.lines[i]; const st=ls[i]||ls[2];
    ctx.font=st.font;
    const pm=st.font.match(/^(.*?)(\d+)(px.*)$/);
    if (pm) { let sz=parseInt(pm[2]); while(ctx.measureText(line).width>CW&&sz>32){sz-=2;ctx.font=`${pm[1]}${sz}${pm[3]}`;} }
    for (const wl of wrapText(line,CW)) {
      ctx.textAlign="left";
      if (st.col==="flex") { ctx.fillStyle="#f0ece8"; }
      else if (st.col==="sting") {
        const tw=ctx.measureText(wl).width;
        const grd=ctx.createLinearGradient(PAD,0,PAD+tw,0);
        grd.addColorStop(0,"#f0b84a"); grd.addColorStop(1,"#d4537e"); ctx.fillStyle=grd;
      } else { ctx.fillStyle="rgba(210,205,228,0.6)"; }
      ctx.fillText(wl,PAD,ly);
      ly+=parseInt((ctx.font.match(/(\d+)px/)||["","60"])[1])*1.45;
    }
    ly+=st.la;
  }
  ly+=28;

  const bt=card.keyword; ctx.font="700 30px sans-serif"; ctx.textAlign="center";
  const bw=ctx.measureText(bt).width+112; const bh=70; const bx=W/2-bw/2; const br=35;
  ctx.beginPath();
  ctx.moveTo(bx+br,ly); ctx.lineTo(bx+bw-br,ly); ctx.arcTo(bx+bw,ly,bx+bw,ly+br,br);
  ctx.lineTo(bx+bw,ly+bh-br); ctx.arcTo(bx+bw,ly+bh,bx+bw-br,ly+bh,br);
  ctx.lineTo(bx+br,ly+bh); ctx.arcTo(bx,ly+bh,bx,ly+bh-br,br);
  ctx.lineTo(bx,ly+br); ctx.arcTo(bx,ly,bx+br,ly,br); ctx.closePath();
  ctx.fillStyle="rgba(107,47,212,0.18)"; ctx.fill();
  ctx.strokeStyle="rgba(155,111,232,0.55)"; ctx.lineWidth=1.5; ctx.stroke();
  ctx.fillStyle="rgba(185,155,240,0.95)"; ctx.fillText(bt,W/2,ly+46);

  const dy=ly+bh+56;
  const dg=ctx.createLinearGradient(PAD,0,W-PAD,0);
  dg.addColorStop(0,"rgba(107,47,212,0)"); dg.addColorStop(0.5,"rgba(155,111,232,0.4)"); dg.addColorStop(1,"rgba(107,47,212,0)");
  ctx.strokeStyle=dg; ctx.lineWidth=1;
  ctx.beginPath(); ctx.moveTo(PAD,dy); ctx.lineTo(W-PAD,dy); ctx.stroke();

  ctx.font="italic 400 40px Georgia,serif"; ctx.fillStyle="rgba(220,216,235,0.45)"; ctx.textAlign="center";
  let qy=dy+72;
  for (const ql of wrapText(`"${card.quote}"`,CW-40)) { ctx.fillText(ql,W/2,qy); qy+=60; }

  ctx.font="400 26px sans-serif"; ctx.fillStyle="rgba(232,228,240,0.18)";
  ctx.textAlign="left"; ctx.fillText("your chart picked you specifically.",PAD,H-52);
  ctx.textAlign="right"; ctx.fillText("bluntchart.com",W-PAD,H-52);

  const bb=ctx.createLinearGradient(0,0,W,0);
  bb.addColorStop(0,"#f0b84a"); bb.addColorStop(0.5,"#d4537e"); bb.addColorStop(1,"#6b2fd4");
  ctx.fillStyle=bb; ctx.fillRect(0,H-10,W,10);

  return cvs;
}

