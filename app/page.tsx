// ─── SWISS EPHEMERIS BIRTH CHART WHEEL ────────────────────────────────────────
// Planets placed at the midpoint of their sign on the ecliptic.
// Aspect detection uses standard orbs (8°).
// Styled with soft pastel colours on a deep dark background.

const SIGN_BASE_DEG: Record<string, number> = {
  Aries:0, Taurus:30, Gemini:60, Cancer:90, Leo:120, Virgo:150,
  Libra:180, Scorpio:210, Sagittarius:240, Capricorn:270, Aquarius:300, Pisces:330,
};

function signToDeg(signStr: string): number {
  const nm = signStr.trim().split(/\s+/)[0];
  return (SIGN_BASE_DEG[nm] ?? 0) + 15;
}

function getAspect(d1: number, d2: number): { color: string } | null {
  let diff = Math.abs(d1 - d2) % 360;
  if (diff > 180) diff = 360 - diff;
  const o = 8;
  if (diff <= o)                  return { color: "rgba(240,210,130,0.55)" };   // conjunction — gold
  if (Math.abs(diff - 60)  <= o)  return { color: "rgba(140,220,190,0.45)" };  // sextile — mint
  if (Math.abs(diff - 90)  <= o)  return { color: "rgba(240,160,180,0.45)" };  // square — rose
  if (Math.abs(diff - 120) <= o)  return { color: "rgba(180,160,240,0.50)" };  // trine — lavender
  if (Math.abs(diff - 180) <= o)  return { color: "rgba(140,200,230,0.45)" };  // opposition — sky
  return null;
}

function BirthChartWheel({ planets, name }: { planets: PlanetMap; name: string }) {
  const S = 300, cx = 150, cy = 150;
  const outerR = 136, zodiacR = 120, zodiacInner = 100, houseR = 92, innerR = 58;
  const toRad = (d: number) => (d - 90) * Math.PI / 180;

  const activePlanets = PLANET_ORDER.filter(k => planets[k as keyof PlanetMap]);

  // Zodiac glyphs and their pastel colours (matching the dark theme)
  const ZODIAC = [
    { g:"♈", c:"#F4A890" }, { g:"♉", c:"#96DCBC" }, { g:"♊", c:"#F4D090" }, { g:"♋", c:"#90C4DC" },
    { g:"♌", c:"#F4D090" }, { g:"♍", c:"#96DCBC" }, { g:"♎", c:"#E09CBE" }, { g:"♏", c:"#BCA0EC" },
    { g:"♐", c:"#F4A890" }, { g:"♑", c:"#B0A8D0" }, { g:"♒", c:"#90C4DC" }, { g:"♓", c:"#BCA0EC" },
  ];

  const symMap: Record<string, string> = {
    sun:"☉", moon:"☽", rising:"AC", venus:"♀", mars:"♂",
    mercury:"☿", saturn:"♄", jupiter:"♃",
  };

  const pDeg  = (k: string) => signToDeg(planets[k as keyof PlanetMap] || "");
  const pPos  = (k: string) => {
    const a = toRad(pDeg(k));
    const r = (houseR + zodiacInner) / 2;
    return { x: cx + r * Math.cos(a), y: cy + r * Math.sin(a) };
  };

  return (
    <svg width={S} height={S} viewBox={`0 0 ${S} ${S}`}
      style={{ display:"block", margin:"0 auto", filter:"drop-shadow(0 0 24px rgba(107,47,212,0.25))" }}>
      <defs>
        <radialGradient id="cg" cx="50%" cy="50%" r="50%">
          <stop offset="0%"   stopColor="rgba(107,47,212,0.18)"/>
          <stop offset="60%"  stopColor="rgba(20,10,50,0.85)"/>
          <stop offset="100%" stopColor="rgba(4,2,14,0.95)"/>
        </radialGradient>
        <radialGradient id="zg" cx="50%" cy="50%" r="50%">
          <stop offset="0%"   stopColor="rgba(60,30,120,0.12)"/>
          <stop offset="100%" stopColor="rgba(4,2,14,0.0)"/>
        </radialGradient>
      </defs>

      {/* ── Outer ring (thin, subtle) ── */}
      <circle cx={cx} cy={cy} r={outerR} fill="none"
        stroke="rgba(155,111,232,0.35)" strokeWidth="1"/>

      {/* ── 5° tick marks ── */}
      {Array.from({length:72}).map((_,i) => {
        const a = toRad(i * 5);
        const isMaj = i % 6 === 0;
        const r1 = outerR - (isMaj ? 10 : 5);
        return (
          <line key={`t${i}`}
            x1={cx + r1*Math.cos(a)} y1={cy + r1*Math.sin(a)}
            x2={cx + outerR*Math.cos(a)} y2={cy + outerR*Math.sin(a)}
            stroke={isMaj ? "rgba(155,111,232,0.5)" : "rgba(155,111,232,0.18)"}
            strokeWidth={isMaj ? "1" : "0.5"}/>
        );
      })}

      {/* ── Zodiac band ── */}
      <circle cx={cx} cy={cy} r={zodiacR}     fill="none" stroke="rgba(107,47,212,0.4)"  strokeWidth="0.75"/>
      <circle cx={cx} cy={cy} r={zodiacInner} fill="none" stroke="rgba(107,47,212,0.3)"  strokeWidth="0.75"/>

      {ZODIAC.map((z, i) => {
        const divA  = toRad(i * 30);
        const midA  = toRad(i * 30 + 15);
        const rSym  = (zodiacR + zodiacInner) / 2;
        return (
          <g key={`z${i}`}>
            {/* divider line from zodiacInner to outer */}
            <line
              x1={cx + zodiacInner*Math.cos(divA)} y1={cy + zodiacInner*Math.sin(divA)}
              x2={cx + outerR*Math.cos(divA)}      y2={cy + outerR*Math.sin(divA)}
              stroke="rgba(107,47,212,0.35)" strokeWidth="0.6"/>
            {/* glyph */}
            <text x={cx + rSym*Math.cos(midA)} y={cy + rSym*Math.sin(midA)}
              textAnchor="middle" dominantBaseline="middle"
              fill={z.c} fontSize="10.5" fontFamily="serif" opacity="0.88">
              {z.g}
            </text>
          </g>
        );
      })}

      {/* ── House ring ── */}
      <circle cx={cx} cy={cy} r={houseR} fill="none"
        stroke="rgba(107,47,212,0.2)" strokeWidth="0.6"/>

      {Array.from({length:12}).map((_,i) => {
        const a    = toRad(i * 30 + 5);
        const midA = toRad(i * 30 + 20);
        return (
          <g key={`h${i}`}>
            <line
              x1={cx + innerR*Math.cos(a)} y1={cy + innerR*Math.sin(a)}
              x2={cx + houseR*Math.cos(a)} y2={cy + houseR*Math.sin(a)}
              stroke="rgba(155,111,232,0.14)" strokeWidth="0.5" strokeDasharray="2,4"/>
            <text
              x={cx + (innerR+13)*Math.cos(midA)}
              y={cy + (innerR+13)*Math.sin(midA)}
              textAnchor="middle" dominantBaseline="middle"
              fill="rgba(155,111,232,0.28)" fontSize="6.5" fontFamily="sans-serif">
              {i+1}
            </text>
          </g>
        );
      })}

      {/* ── Inner circle — gradient fill + subtle glow ring ── */}
      <circle cx={cx} cy={cy} r={innerR}   fill="url(#cg)" stroke="rgba(107,47,212,0.4)" strokeWidth="0.8"/>
      <circle cx={cx} cy={cy} r={innerR-5} fill="url(#zg)" stroke="none"/>

      {/* ── Aspect lines (inside innerR) ── */}
      {activePlanets.map((k, idx) => {
        const d1  = pDeg(k);
        const ix1 = cx + (innerR-2)*Math.cos(toRad(d1));
        const iy1 = cy + (innerR-2)*Math.sin(toRad(d1));
        return activePlanets.slice(idx+1).map(k2 => {
          const d2  = pDeg(k2);
          const ix2 = cx + (innerR-2)*Math.cos(toRad(d2));
          const iy2 = cy + (innerR-2)*Math.sin(toRad(d2));
          const asp = getAspect(d1, d2);
          return (
            <line key={`a${k}${k2}`}
              x1={ix1} y1={iy1} x2={ix2} y2={iy2}
              stroke={asp ? asp.color : "rgba(155,111,232,0.08)"}
              strokeWidth={asp ? "0.9" : "0.35"}/>
          );
        });
      })}

      {/* ── Planet connector lines (rim → dot) ── */}
      {activePlanets.map(k => {
        const pos  = pPos(k);
        const col  = DOT_COLORS[k] || "#9B6FE8";
        const rimX = cx + innerR*Math.cos(toRad(pDeg(k)));
        const rimY = cy + innerR*Math.sin(toRad(pDeg(k)));
        return (
          <line key={`c${k}`}
            x1={rimX} y1={rimY} x2={pos.x} y2={pos.y}
            stroke={col} strokeWidth="0.6" opacity="0.45"/>
        );
      })}

      {/* ── Planet dots + symbols ── */}
      {activePlanets.map(k => {
        const pos    = pPos(k);
        const col    = DOT_COLORS[k] || "#9B6FE8";
        const sym    = symMap[k] || k[0].toUpperCase();
        const isAC   = sym === "AC";
        const isRetro = (planets[k as keyof PlanetMap] || "").toLowerCase().includes("retro");
        return (
          <g key={`p${k}`}>
            {/* outer glow */}
            <circle cx={pos.x} cy={pos.y} r={9}   fill={col} opacity="0.08"/>
            <circle cx={pos.x} cy={pos.y} r={5.5} fill={col} opacity="0.18"/>
            {/* dot */}
            <circle cx={pos.x} cy={pos.y} r={4.5} fill={col} opacity="0.92"/>
            {/* glyph above dot */}
            <text x={pos.x} y={pos.y - 8}
              textAnchor="middle" fill={col}
              fontSize={isAC ? "5.5" : "8.5"} fontWeight="700"
              fontFamily="serif" opacity="0.95">
              {sym}
            </text>
            {isRetro && (
              <text x={pos.x+6} y={pos.y-6} textAnchor="middle"
                fill={col} fontSize="5.5" fontFamily="sans-serif" opacity="0.7">
                ℞
              </text>
            )}
          </g>
        );
      })}

      {/* ── Centre label ── */}
      <text x={cx} y={cy-6} textAnchor="middle"
        fill="rgba(232,228,240,0.14)" fontSize="5.5" fontWeight="700"
        letterSpacing="2" fontFamily="sans-serif">
        {name.slice(0,8).toUpperCase()}
      </text>
      <text x={cx} y={cy+7} textAnchor="middle"
        fill="rgba(155,111,232,0.55)" fontSize="11" fontFamily="serif">✦</text>

      {/* ── Aspect legend ── */}
      {[
        { label:"△", col:"rgba(180,160,240,0.7)",  x:cx-42 },
        { label:"□", col:"rgba(240,160,180,0.7)",  x:cx-14 },
        { label:"✶", col:"rgba(140,220,190,0.7)",  x:cx+14 },
        { label:"☍", col:"rgba(140,200,230,0.7)",  x:cx+42 },
      ].map(l => (
        <text key={l.label} x={l.x} y={S-6}
          textAnchor="middle" fill={l.col}
          fontSize="7" fontFamily="serif" opacity="0.75">
          {l.label}
        </text>
      ))}
    </svg>
  );
}

